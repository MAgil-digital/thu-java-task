package src.main.java.KAP1;

public class DeclarationAndInitialization {
    public static void main (String [] args){

        //mark where to declare and initialize

        int number; //declaration
        String word; //declaration
        double realNumber;//declaration
        number = 1; //initialization
        word = "Hello"; //initialization
        realNumber = 0.2; //initialization
        System.out.println(number);
        System.out.println(word);
        System.out.println(realNumber);
        int anotherNumber = 5; //declaration & initialization
        String anotherWord = "You"; //declaration 6 initialization
        double anotherRealNumber = 8.15; // declaration & initialization
        System.out.println(anotherNumber);
        System.out.println(anotherWord);
        System.out.println(anotherRealNumber);
    }
}
